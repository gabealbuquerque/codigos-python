# Programa que lê um número inteiro e mostra seu sucessor e antecessor
# Pedindo número
num = int(input('Digite um número: '))
# Analisando antecessor
antecessor = num - 1
# Analisando sucessor
sucessor = num + 1
# Imprimindo na tela
print(f'O antecessor de {num} é {antecessor}\n O sucessor de {num} é {sucessor}')
